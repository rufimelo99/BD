CREATE TABLE CLIENTE (
	
	Num_Telefone VARCHAR(15),
	Morada VARCHAR(50) NOT NULL,
	Nome VARCHAR(30) NOT NULL,
	NIF CHAR(10),
	PRIMARY KEY (Num_Telefone));

CREATE TABLE HOMEM_ENTREGAS (
	ID_Funcionario CHAR(3)
	PRIMARY KEY (ID_Funcionario));

CREATE TABLE COZINHEIRO (
	
	ID_Cozinheiro CHAR(2),
	PRIMARY KEY (ID_Cozinheiro));

CREATE TABLE PEDIDO (
	
	Num_Pedido CHAR(4) UNIQUE,
	Num_Telefone_Cliente VARCHAR(15),
	Funcionario_ID CHAR(3),
	Cozinheiro_ID CHAR(2),
	/*Metodo VARCHAR(15),
		CHECK (Metodo = 'DINHEIRO' OR Metodo = 'MULTIBANCO')*/
	PRIMARY KEY (Num_Pedido,Funcionario_ID),
	FOREIGN KEY (Funcionario_ID) REFERENCES HOMEM_ENTREGAS(ID_Funcionario)
		ON DELETE SET NULL,
	FOREIGN KEY (Num_Telefone_Cliente) REFERENCES CLIENTE(Num_Telefone)
		ON DELETE SET NULL,
	FOREIGN KEY (Cozinheiro_ID) REFERENCES COZINHEIRO(ID_Cozinheiro)
		ON DELETE SET NULL,);

CREATE TABLE AVALIACAO (

	Nu_Pedido CHAR(4),
	Comentario VARCHAR(100),
	PRIMARY KEY (Nu_Pedido),
	FOREIGN KEY (Nu_Pedido) REFERENCES PEDIDO(Num_Pedido));

CREATE TABLE VEICULO (
	
	Matricula VARCHAR(8),
	Funcionario_ID CHAR(3),
	Tipo_Veiculo VARCHAR(15),
		CHECK (Tipo_Veiculo = 'Mota' OR Tipo_Veiculo = 'Carrinha'),
	Capacidade_Pedidos CHAR(2)
		CHECK (Capacidade_Pedidos = 3 OR Capacidade_Pedidos = 6),
	PRIMARY KEY (Matricula,Funcionario_ID),
	FOREIGN KEY (Funcionario_ID) REFERENCES HOMEM_ENTREGAS(ID_Funcionario));

